// Bucle for que imprime números del 1 al 5
for (let i = 1; i <= 5; i++) {
    console.log(i);
}

// Bucle while que imprime números del 1 al 5
let j = 1;
while (j <= 5) {
    console.log(j);
    j++;
}

// Bucle do-while que imprime números del 1 al 5
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 5);
