// Operadores de asignación
let x = 10;
x += 5;   // Equivalente a x = x + 5; (15)
x -= 3;   // Equivalente a x = x - 3; (12)
x *= 2;   // Equivalente a x = x * 2; (24)
x /= 4;   // Equivalente a x = x / 4; (6)
x %= 2;   // Equivalente a x = x % 2; (0)

console.log(x)

// Operadores de comparación
let a = 10;
let b = 5;

console.log(a == b);   // false (igualdad)
console.log(a != b);   // true (desigualdad)
console.log(a > b);    // true (mayor que)
console.log(a < b);    // false (menor que)
console.log(a >= b);   // true (mayor o igual que)
console.log(a <= b);   // false (menor o igual que)

// Operadores lógicos
let c = true;
let d = false;

console.log(c && d);   // false (AND lógico)
console.log(c || d);   // true (OR lógico)
console.log(!c);       // false (NOT lógico)

